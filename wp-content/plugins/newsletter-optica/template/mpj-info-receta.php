        <h4 class="title-receta">Cómo interpretar los datos de la receta de gafas</h4>
        <p class="info-receta">Si has tenido en algún momento en tus manos una receta de la graduación de las gafas
            habrás observado un montón de datos con signos difíciles de comprender. Aquí vamos a esclarecer punto por
            punto cada uno de estos datos para que entiendas bien qué corrección necesita tu vista.</p>
        <br>

        <h5 class="title-receta">¿Que significa OD u OS?:</h5>
        <p class="info-receta">
            Lo primero que hay que saber es que significa “OD” o “OI”. Son abreviaciones para ojo derecho (OD) y ojo
            izquierdo (OI u OS), algunos médicos lo abrevian como OS “oculus sinister” en latín.

            También puede utilizarse “AO” para referirse a ambos ojos, cuando son lentes que en ambos tienen la misma
            graduación, como en los lentes pregraduados.
        </p>


        <h5 class="title-receta">Esfera: (E)</h5>
        <p class="info-receta">
            Indica el poder del lente y se mide en dioptrías. Es el "nivel" de miopía o hipermetropía que se tiene. Si
            el número aparece en negativo (-) es miopía (mala visión de lejos). Si el número aparece en positivo (+) es
            hipermetropía (no se ve bien de cerca).
        </p>

        <h5 class="title-receta">Cilindro: (CIL)</h5>
        <p class="info-receta">
            Indica el astigmatismo que se tiene. Casi siempre es negativo en los paises de latinoamérica. Si no aparece
            ningún numero en este espacio, significa que no tiene astigmatismo.
            El término cilindro o astigmatismo es que el poder del lente no corrige igual en todas las direcciones, es
            cuando el ojo tiene forma ovalada, cuando debe ser redonda.
        </p>
        <h5 class="title-receta">Eje:</h5>
        <p class="info-receta">
            Es un número entre 0 y 180 grados, y es el complemento del astigmatismo, menciona hacia donde va dirigido el
            poder del astigmatismo y no indica nivel de graduación. El eje siempre va después del cilindro.
        </p>
        <h5 class="title-receta">Adiccion: (ADD)</h5>
        <p class="info-receta">
            o agregado es la magnificación que se aplica en los lentes para visión de cerca, se llaman bifocales o
            progresivos, para corregir la presbicia (o vista cansada). Generalmente van de un rango de +1.00 a +3.00 y
            por lo común se escribe el mismo poder en ambos ojos
        </p>
        <h5 class="title-receta">Tipo de lente</h5>
        <p class="info-receta">
            Después de saber la graduación es necesario si los lentes serán monofocales (que solo tengan una graduación)
            o con agregado para ver de cerca (bifocales o progresivos).
        </p>
